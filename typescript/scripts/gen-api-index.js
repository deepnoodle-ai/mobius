#!/usr/bin/env node
// gen-api-index.js — generate src/api/index.ts from the openapi-typescript
// output in src/api/schema.ts.
//
// Reads schema.ts, extracts every named key under `components.schemas`, and
// emits a flat type alias for each one. This replaces the hand-maintained
// index.ts that previously had to be updated whenever the OpenAPI spec renamed
// a schema.
//
// Usage:  node scripts/gen-api-index.js
//         (or via `pnpm generate`, which chains this after openapi-typescript)

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const schemaPath = join(__dirname, "..", "src", "api", "schema.ts");
const outPath = join(__dirname, "..", "src", "api", "index.ts");

const src = readFileSync(schemaPath, "utf-8");

// The generated schema.ts has a `schemas: { ... }` block inside the
// `components` interface. Each top-level key in that block is a schema name.
// We extract them with a simple regex that matches lines like:
//   "        SchemaName: {"
// inside the schemas block.

const schemaBlockRe = /^\s+schemas:\s*\{/m;
const match = schemaBlockRe.exec(src);
if (!match) {
  console.error("gen-api-index: could not find schemas block in schema.ts");
  process.exit(1);
}

const names = [];
let depth = 0;
let inSchemas = false;

// Walk line by line from the schemas block start.
const lines = src.slice(match.index).split("\n");
for (const line of lines) {
  if (!inSchemas) {
    if (line.match(/^\s+schemas:\s*\{/)) {
      inSchemas = true;
      depth = 1;
    }
    continue;
  }

  // Count braces to track depth within the schemas block.
  for (const ch of line) {
    if (ch === "{") depth++;
    else if (ch === "}") depth--;
  }

  // At depth 1 we're directly inside schemas: { ... }. Lines that define a
  // new schema key look like:  "        SchemaName: {" or with a JSDoc
  // comment above. We only care about the key declaration lines.
  // A key at depth 1 means we just went from depth 1 -> 2 on this line.
  const keyMatch = line.match(/^\s{8}(\w+):\s*\{/);
  if (keyMatch && depth === 2) {
    names.push(keyMatch[1]);
  }

  // Also catch single-line enum-like schemas:  "SchemaName: value;"
  const inlineMatch = line.match(/^\s{8}(\w+):\s*[^{]/);
  if (inlineMatch && depth === 1) {
    names.push(inlineMatch[1]);
  }

  if (depth === 0) break;
}

if (names.length === 0) {
  console.error("gen-api-index: found no schema names — check schema.ts format");
  process.exit(1);
}

// Sort for stable output.
names.sort();

const header = `// Code generated by scripts/gen-api-index.js. DO NOT EDIT.
//
// Regenerate with:  pnpm generate
//
// Flat type aliases for every OpenAPI schema so SDK code can import clean
// names instead of components["schemas"]["..."] paths.

import type { components } from "./schema.js";
export type { components, operations } from "./schema.js";
`;

const aliases = names
  .map((name) => `export type ${name} = components["schemas"]["${name}"];`)
  .join("\n");

const out = header + "\n" + aliases + "\n";

writeFileSync(outPath, out, "utf-8");
console.log(`gen-api-index: wrote ${names.length} type aliases to src/api/index.ts`);
